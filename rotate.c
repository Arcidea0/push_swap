#include "push_swap.h"

void	rotate(t_list **stack)
{
	t_list	*tmp;
	t_list	*skizb;

	if (ft_lstsize(*stack) < 2)
		return ;
	skizb = *stack;
	tmp = ft_lstlast(*stack);
	*stack = skizb->next;
	skizb->next = NULL;
	tmp->next = skizb;
}

void	ra(t_list **a)
{
	rotate(a);
	write(1, "ra\n", 3);
}

void	rb(t_list **b)
{
	rotate(b);
	write(1, "rb\n", 3);
}

void	rr(t_list **a, t_list **b)
{
	rotate(a);
	rotate(b);
	write(1, "rr\n", 3);
}
